const QulaylikItem = ({
  icon,
  title,
  value,
  setPayload,
  amenities
}: {
  icon: any;
  value: any;
  title: { uz: string; ru: string; en: string };
  amenities: string[] | undefined;
  setPayload: React.Dispatch<React.SetStateAction<PlayerPayload>> | undefined;
}) => {
  const t = useTranslation();
  if (!icon || !title) return null;
  const activeColor = () => {
    if (amenities?.includes(value)) {
      return '#ECFCE5';
    }
    return '#F2F4F5';
  };
  const activeColor2 = () => {
    if (amenities?.includes(value)) {
      return '#42BA3D';
    }
    return '#979C9E';
  };
  return (
    <div
      onClick={() => {
        if (amenities?.includes(value)) {
          setPayload?.((p) => ({
            ...p,
            amenities: p?.amenities?.filter((i) => i !== value)
          }));
        } else {
          setPayload?.((p) => ({
            ...p,
            amenities: [...(p?.amenities || []), value]
          }));
        }
      }}
    >
      <div
        className={`p-3 transition-all duration-200 ease-in-out rounded flex items-center justify-center min-h-[50px] bg-[${activeColor()}]`}
      >
        {icon}
      </div>
      <h1
        style={{
          color: `${activeColor2()}`
        }}
        className={`text-[12px] transition-all duration-200 ease-in-out font-medium text-gray-800 text-center `}
      >
        {t(title)}
      </h1>
    </div>
  );
};
